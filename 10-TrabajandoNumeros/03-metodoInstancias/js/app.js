"use strict"

var numero = 2.5;

// to exponential (metodos sobre numeros exponenciales)
console.log("toExponential: ", numero.toExponential(4));

// to fixed (regresa a numero entero en decimales)
console.log("toFixed: ", numero.toFixed(5));

// to precision 
console.log("toExponential: ", numero.toPrecision(4));

// to string convertir un numero a cadena de texto
console.log("toExponential: ", numero.toString());