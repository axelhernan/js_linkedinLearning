"use strict"
var datoA = 10;

// asignacion simple
var igual = datoA;
console.log("el resultado de la signacion es = " + igual)

// suma y asignar 
var masIgual = 10;
masIgual += datoA;
console.log("el resultado de la signacion es = " + masIgual)

// menos y asignar 
var menosIgual = 10;
menosIgual -= datoA;
console.log("el resultado de la signacion es = " + menosIgual)

// por y asignar
var porIgual = 10;
porIgual *= datoA;
console.log("el resultado de la signacion es = " + porIgual)

// dividir y asignar
var entreIgual = 20;
entreIgual /= datoA;
console.log("el resultado de la signacion es = " + entreIgual)