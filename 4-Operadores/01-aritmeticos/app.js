"use strict"

var datoA = 10;
var datoB = 20;

// suma
var suma = datoA + datoB;
console.log("el resultado es " + suma);

// resta
var resta = datoA - datoB;
console.log("el resultado es " + resta);


// multiplicacion
var multiplicacion = datoA * datoB;
console.log("el resultado es " + multiplicacion);


// division
var division = datoA / datoB;
console.log("el resultado es " + division);


// modulo
var modulo = datoA % datoB;
console.log("el resultado es " + modulo);

// incremendo
var incremento = datoA;
incremento++;
console.log("el resultado es " + incremento);


// decremento
var decremento = datoA;
decremento--;
console.log("el resultado es " + decremento);


