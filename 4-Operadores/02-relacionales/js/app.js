"use strict"

var datoA = 10;
var datoB = 20;

//Mayor que > 
var mayor = datoA > datoB;
console.log("es mayor que " + mayor);

//Menor que <
var menor = datoA < datoB;
console.log("es menor que " + menor);

// Mayor o igual que >=
var mayorIgual = datoA >= datoB;
console.log("es mayor o igual que " + mayorIgual);

// Menor o igual que <=
var menorIgual = datoA <= datoB;
console.log("es menor o igual que " + menorIgual);

// Igual que ==
var igual = datoA == datoB;
console.log("es igual que " + igual);

// no es igual que o es diferente que !=
var diferente = datoA != datoB;
console.log("es diferente que " + diferente);
