"use strict"

// switch caso numero
var numero = 10;
var resultado = "";

switch (numero) {
    case 10:
        resultado = "la edad es 10"
        break;

    case 20:
        resultado = "la edad es 20"
        break;

    case 30:
        resultado = "la edad es 30"
        break;

    default:
        break;
}

console.log(resultado)